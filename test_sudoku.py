import unittest
from sudoku import *

class TestSudoku(unittest.TestCase):
    def setUp(self):
        # Set up a sample Sudoku board
        self.board = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]

    def test_is_valid_move(self):
        # Test that a valid move is recognized as valid
        self.assertTrue(is_valid_move(self.board, 2, (0, 2)))

        # Test that an invalid move is recognized as invalid
        self.assertFalse(is_valid_move(self.board, 3, (0, 2)))

    def test_find_empty_cell(self):
        # Test that an empty cell is found
        self.assertEqual(find_empty_cell(self.board), (0, 2))

        # Test that no empty cell is found
        board = [
            [5, 3, 4, 6, 7, 8, 9, 1, 2],
            [6, 7, 2, 1, 9, 5, 3, 4, 8],
            [1, 9, 8, 3, 4, 2, 5, 6, 7],
            [8, 5, 9, 7, 6, 1, 4, 2, 3],
            [4, 2, 6, 8, 5, 3, 7, 9, 1],
            [7, 1, 3, 9, 2, 4, 8, 5, 6],
            [9, 6, 1, 5, 3, 7, 2, 8, 4],
            [2, 8, 7, 4, 1, 9, 6, 3, 5],
            [3, 4, 5, 2, 8, 6, 1, 7, 9]
        ]
        self.assertIsNone(find_empty_cell(board))

   